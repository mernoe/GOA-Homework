# რიცხვები 50დან 100მდე.
for num in range(50, 100):
    print(num)

print("-------------")

# რიცხვები 10დან 1ის ჩათვლით.
for num in range(10, 0, -1):
    print(num)

print("-------------")

# Hello world-ის 100ჯერ გამოტანა
for _ in range(100):
    print("Hello world")

print("-------------")

# დათვლა
counter = 1

for _ in range(100):
    print(counter)
    print("Hello world")
    
    counter += 1
